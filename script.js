// Load featured posts
function loadFeaturedPosts() {
    const container = document.getElementById('featured-posts');
    if (container) {
        const featured = blogPosts.filter(post => post.featured);
        container.innerHTML = featured.map(post => createBlogCard(post)).join('');
    }
}

// Load all posts
function loadAllPosts() {
    const container = document.getElementById('blog-posts');
    if (container) {
        container.innerHTML = blogPosts.map(post => createBlogCard(post)).join('');
    }
}

// Search and filter
function setupSearchAndFilter() {
    const searchInput = document.getElementById('search-input');
    const filterButtons = document.querySelectorAll('.filter-btn');
    const postsContainer = document.getElementById('blog-posts');
    const noPosts = document.getElementById('no-posts');
    
    let currentCategory = 'all';
    let currentSearch = '';
    
    function filterPosts() {
        let filtered = blogPosts;
        
        if (currentCategory !== 'all') {
            filtered = filtered.filter(post => post.category === currentCategory);
        }
        
        if (currentSearch) {
            filtered = filtered.filter(post => 
                post.title.toLowerCase().includes(currentSearch.toLowerCase()) ||
                post.excerpt.toLowerCase().includes(currentSearch.toLowerCase())
            );
        }
        
        if (filtered.length > 0) {
            postsContainer.innerHTML = filtered.map(post => createBlogCard(post)).join('');
            noPosts.style.display = 'none';
        } else {
            postsContainer.innerHTML = '';
            noPosts.style.display = 'block';
        }
    }
    
    if (searchInput) {
        searchInput.addEventListener('input', (e) => {
            currentSearch = e.target.value;
            filterPosts();
        });
    }
    
    filterButtons.forEach(button => {
        button.addEventListener('click', () => {
            filterButtons.forEach(btn => btn.classList.remove('active'));
            button.classList.add('active');
            currentCategory = button.dataset.category;
            filterPosts();
        });
    });
}

