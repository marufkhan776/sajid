function showModal(post) {
    const modal = document.getElementById('blog-modal');
    const modalTitle = document.getElementById('modal-title');
    const modalImage = document.getElementById('modal-image');
    const modalCategory = document.getElementById('modal-category');
    const modalDate = document.getElementById('modal-date');
    const modalBody = document.getElementById('modal-body');
    
    modalTitle.textContent = post.title;
    modalImage.src = post.image;
    modalImage.alt = post.title;
    modalCategory.textContent = post.category;
    modalDate.innerHTML = `<i class="fas fa-calendar"></i> ${post.date}`;
    modalBody.innerHTML = post.content;
    
    modal.classList.add('show');
    document.body.style.overflow = 'hidden';
}

function hideModal() {
    const modal = document.getElementById('blog-modal');
    modal.classList.remove('show');
    document.body.style.overflow = 'auto';
}

function setupModal() {
    const modal = document.getElementById('blog-modal');
    const modalOverlay = document.getElementById('modal-overlay');
    const modalClose = document.getElementById('modal-close');
    
    // Close modal on overlay click
    modalOverlay.addEventListener('click', hideModal);
    
    // Close modal on close button click
    modalClose.addEventListener('click', hideModal);
    
    // Close modal on escape key
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && modal.classList.contains('show')) {
            hideModal();
        }
    });
    
    // Add click event to blog cards
    document.addEventListener('click', (e) => {
        const blogCard = e.target.closest('.blog-card');
        if (blogCard) {
            const postId = parseInt(blogCard.dataset.postId);
            const post = blogPosts.find(p => p.id === postId);
            if (post) {
                showModal(post);
            }
        }
    });
}

// Mobile Navigation
function setupMobileNav() {
    const navMenu = document.getElementById('nav-menu');
    const navToggle = document.getElementById('nav-toggle');
    const navLinks = document.querySelectorAll('.nav__link');
    
    // Toggle mobile menu
    if (navToggle) {
        navToggle.addEventListener('click', () => {
            navMenu.classList.toggle('show-menu');
        });
    }
    
    // Close mobile menu when clicking on a link
    navLinks.forEach(link => {
        link.addEventListener('click', () => {
            navMenu.classList.remove('show-menu');
        });
    });
    
    // Close mobile menu when clicking outside
    document.addEventListener('click', (e) => {
        if (!e.target.closest('.nav') && navMenu.classList.contains('show-menu')) {
            navMenu.classList.remove('show-menu');
        }
    });
}

